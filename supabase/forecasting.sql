-- Forecasting tables for the demand planning workflow.
-- Run this against an existing Supabase database that already has the core app tables.

CREATE TABLE IF NOT EXISTS public.forecast_categories (
  id integer PRIMARY KEY,
  name text NOT NULL
);

CREATE TABLE IF NOT EXISTS public.forecast_outlets (
  id integer PRIMARY KEY,
  name text NOT NULL
);

CREATE TABLE IF NOT EXISTS public.forecast_products (
  id integer PRIMARY KEY,
  name text NOT NULL,
  category_id integer REFERENCES public.forecast_categories(id),
  current_stock integer NOT NULL,
  reorder_level integer NOT NULL
);

CREATE TABLE IF NOT EXISTS public.historical_orders (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id integer NOT NULL REFERENCES public.forecast_products(id),
  outlet_id integer NOT NULL REFERENCES public.forecast_outlets(id),
  order_date date NOT NULL,
  quantity integer NOT NULL,
  buyer_type text NOT NULL DEFAULT 'standard'
);

CREATE TABLE IF NOT EXISTS public.forecasts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id integer NOT NULL REFERENCES public.forecast_products(id),
  outlet_id integer NOT NULL REFERENCES public.forecast_outlets(id),
  forecast_date date NOT NULL,
  predicted_demand numeric NOT NULL,
  confidence_lower numeric,
  confidence_upper numeric,
  generated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.forecast_jobs (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  outlet_id integer NOT NULL REFERENCES public.forecast_outlets(id),
  status text NOT NULL DEFAULT 'queued',
  progress integer NOT NULL DEFAULT 0,
  message text,
  result jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  started_at timestamptz,
  completed_at timestamptz
);

ALTER TABLE public.forecast_categories ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.forecast_outlets ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.forecast_products ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.historical_orders ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.forecasts ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.forecast_jobs ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Enable read access for forecasting tables" ON public.forecast_categories FOR SELECT USING (true);
CREATE POLICY "Enable read access for forecasting tables" ON public.forecast_outlets FOR SELECT USING (true);
CREATE POLICY "Enable read access for forecasting tables" ON public.forecast_products FOR SELECT USING (true);
CREATE POLICY "Enable read access for forecasting tables" ON public.historical_orders FOR SELECT USING (true);
CREATE POLICY "Enable read access for forecasting tables" ON public.forecasts FOR SELECT USING (true);
CREATE POLICY "Enable read access for forecasting tables" ON public.forecast_jobs FOR SELECT USING (true);

CREATE POLICY "Enable insert for forecasting tables" ON public.historical_orders FOR INSERT WITH CHECK (true);
CREATE POLICY "Enable insert for forecasting tables" ON public.forecasts FOR INSERT WITH CHECK (true);
CREATE POLICY "Enable insert for forecasting tables" ON public.forecast_jobs FOR INSERT WITH CHECK (true);

CREATE POLICY "Enable update for forecasting tables" ON public.forecasts FOR UPDATE USING (true) WITH CHECK (true);
CREATE POLICY "Enable update for forecasting tables" ON public.forecast_jobs FOR UPDATE USING (true) WITH CHECK (true);

CREATE POLICY "Enable delete for forecasting tables" ON public.historical_orders FOR DELETE USING (true);
CREATE POLICY "Enable delete for forecasting tables" ON public.forecasts FOR DELETE USING (true);
CREATE POLICY "Enable delete for forecasting tables" ON public.forecast_jobs FOR DELETE USING (true);

